const { Products, Shops } = require("../models");
const { Op } = require("sequelize");

const createProduct = async (req, res) => {
  const { name, stock, price, shopId } = req.body;

  try {
    const newProduct = await Products.create({
      name,
      stock,
      price,
      shopId,
    });

    res.status(201).json({
      status: "Success",
      message: "Success create new product",
      isSuccess: true,
      data: {
        newProduct,
      },
    });
  } catch (error) {
    console.log(error.name);
    if (error.name === "SequelizeValidationError") {
      const errorMessage = error.errors.map((err) => err.message);
      return res.status(400).json({
        status: "Failed",
        message: errorMessage[0],
        isSuccess: false,
        data: null,
      });
    } else if (error.name === "SequelizeDatabaseError") {
      return res.status(400).json({
        status: "Failed",
        message: error.message || "Database error",
        isSuccess: false,
        data: null,
      });
    } else {
      return res.status(500).json({
        status: "Failed",
        message: "An unexpected error occurred",
        isSuccess: false,
        data: null,
      });
    }
  }
};

const getAllProduct = async (req, res) => {
  try {
    const { name, stock, price, page = 1, perPage = 10 } = req.query;

    const condition = {};
    if (name) condition.name = { [Op.iLike]: `%${name}%` };
    if (stock) condition.stock = stock;
    if (price) condition.price = price;

    const limit = parseInt(perPage, 10);
    const offset = (parseInt(page, 10) - 1) * limit;

    const { count, rows: products } = await Products.findAndCountAll({
      include: [
        {
          model: Shops,
          as: "shop",
        },
      ],
      where: condition,
      limit,
      offset,
    });

    const totalData = count;
    const totalPages = Math.ceil(totalData / limit);

    res.status(200).json({
      data: products,
      meta: {
        totalData,
        page: parseInt(page, 10),
        perPage: limit,
        totalPages,
        statusCode: 200,
        message: "Data was successfully retrieved!",
      },
    });
  } catch (error) {
    console.log(error.name);
    if (error.name === "SequelizeValidationError") {
      const errorMessage = error.errors.map((err) => err.message);
      return res.status(400).json({
        status: "Failed",
        message: errorMessage[0],
        isSuccess: false,
        data: null,
      });
    }

    res.status(500).json({
      status: "Failed",
      message: error.message,
      isSuccess: false,
      data: null,
    });
  }
};

